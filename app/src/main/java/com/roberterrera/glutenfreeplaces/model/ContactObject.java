public class ContactObject {
    private int phone;
    private int formattedPhone;
    private int twitter;

    public int getPhone() {
        return phone;
    }

    public void setPhone(int phone) {
        this.phone = phone;
    }

    public int getFormattedPhone() {
        return formattedPhone;
    }

    public void setFormattedPhone(int formattedPhone) {
        this.formattedPhone = formattedPhone;
    }

    public int getTwitter() {
        return twitter;
    }

    public void setTwitter(int twitter) {
        this.twitter = twitter;
    }

}
